package sample;//현재 파일의 위치정보를 나타내는 코드(가장 먼저 작성해야함)

public class HelloWorld {//클래스, 프로그램의 기본 단위(파일마다 같은 이름으로 한 개씩 존재함)
	public static void main(String[] args) {//메인 메소드, 프로그램의 시작점(형태가 정해짐)
		//첫 번째 프로그램
		System.out.println("Hello world!");//출력 명령, 한 줄에 걸쳐 원하는 글자를 출력
		System.out.println("안녕 세상!");
	}
}

